def get_char_bitmap(ch: str):
    """
    Возвращает bitmap 7x4 в формате:
    [
      [0,1,1,0],
      [1,0,0,1],
      ...
    ]
    """
    ch = ch.upper()
    ascii_char = FONT_7x4_ASCII.get(ch, FONT_7x4_ASCII[" "])

    bitmap = []
    for row in ascii_char:
        bitmap.append([1 if c == "#" else 0 for c in row])

    return bitmap
